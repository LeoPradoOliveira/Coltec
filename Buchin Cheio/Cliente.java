package Buchin.Cheio;
public class Cliente {
    //Atributos
    private String nome;
    private String email;

    //Getters & Setters
    public String getNome() {
        return nome;
    }
    public String getEmail() {
        return email;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public void setEmail(String email) {
        this.email = email;
    }
}
